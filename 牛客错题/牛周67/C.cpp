#include <iostream>
#include <cmath>
using namespace std;
int n,C,count=0;

int main() {
    
    cin>>n>>C;
    n-=2; //ä¸¤ä¸ªç¬¦å·
    for(int i=0;i<=C;i++){
        int wa,wb;
        if(i==0) wa=1;
        else wa=(int)log10(i)+1;
        
        if((C-i)==0) wb=1;
        else wb=(int)log10(C-i)+1;
        
        if((n-wb-wa-(int)log10(C)-1)==0) count++;
    }
    
    cout<<count;
}
